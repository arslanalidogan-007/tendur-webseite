<h1>Wir begrüßen Sie herzlich im TENDUR.</h1>


<p>
TENDUR, so wird in unserer Heimat ein Lehmofen genannt, 
der mit Buchen- und Eichenholz beheizt wird. In ihm werden Lamm-, Kalb- und Putenfleisch gegrillt 
und geschmort, aber auch köstliche Brotwaren gebacken. So vielseitig zu sein, wie dieser traditionelle 
Lehmofen, ist unser Ziel. Wir möchten Ihnen mit ausgesuchten Spezialitäten aus der Traditionsreichen 
Küche Anatoliens und Mesopotamiens ein besonderes Essvergnügen bereiten. Lassen Sie sich zum Beispiel 
von unseren Grillspezialitäten aus unserem Holzkohlegrill verwöhnen. Unsere hausgemachten und stets frisch 
zubereiteten Gerichte treffen ganz bestimmt Ihren Geschmack. Haben Sie Fragen oder einen speziellen Wunsch, 
so sprechen Sie uns einfach an! Ihre Zufriedenheit liegt uns am Herzen.
</p>

